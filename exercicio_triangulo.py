'''Faça um Algoritmo que receba o
valor da base e da altura de um
triângulo e calcule sua área. usando a
fórmula A = (base x Altura ) /2'''

base = float(input("Digite o valor da base do triangulo: "))
altura = float(input("Digite o valor da altura do triangulo: "))

area = (base+altura)/2
print(f"O valor da area é de {area}")